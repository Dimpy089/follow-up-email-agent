const runInvoiceAgent = async () => {
    const invoices = await readCSV();

    const enriched = invoices.map((invoice) => {
        const daysOverdue = calculateEscalationLevel(invoice.dueDate);
        const followUp = getFollowUpStage(daysOverdue);

        return {
            ...invoice,
            daysOverdue,
            stage: followUp.stage,
            tone: followUp.tone
        };
    });

    const results = [];

    for (const invoice of enriched) {
        if (invoice.daysOverdue <= 0) continue;

        if (invoice.stage === "ESCALATED") {
            continue;
        }

        const generatedEmail = await generateEmail(invoice);

        await saveLog({
            invoiceNumber: invoice.invoiceNumber,
            clientName: invoice.clientName,
            tone: invoice.tone,
            stage: invoice.stage,
            generatedEmail,
            timestamp: new Date(),
            status: "AGENT-GENERATED"
        });

        results.push({
            invoice,
            generatedEmail
        });
    }

    return {
        output: `Generated ${results.length} emails successfully`,
        results
    };
};

module.exports = { runInvoiceAgent };